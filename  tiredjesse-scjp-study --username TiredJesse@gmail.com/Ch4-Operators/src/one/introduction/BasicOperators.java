package one.introduction;

public class BasicOperators {
	public static void main(String... operators) {
		float no1 = 4;
		float no2 = 9;
		float no3;
		
		//add
		no3 = no1 + no2;
		System.out.println("Add: " + no3);
		
		//subtract
		no3 = no1 - no2;
		System.out.println("Subtract: " + no3);
		
		//multiply
		no3 = no1 * no2;
		System.out.println("Multiply: " + no3);
		
		//divide
		no3 = no1 / no2;
		System.out.println("Divide: " + no3);
		
		//modulus
		no3 = no1 % no2;
		System.out.println("Modulus: " + no3);
	}

}
